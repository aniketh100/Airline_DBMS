-- Create the database
CREATE DATABASE AIRLINE_MANAGEMENT;

-- Use the newly created database
USE AIRLINE_MANAGEMENT;

-- Create the AIRCRAFT table
CREATE TABLE AIRCRAFT (
    AIRCRAFT_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    MANUFACTURER VARCHAR(40),
    MODEL VARCHAR(40),
    NO_OF_SEATS INT
);

-- Create the AIRPORTS table
CREATE TABLE AIRPORTS (
    AIRPORT_CODE VARCHAR(20) PRIMARY KEY NOT NULL,
    CITY VARCHAR(40),
    COUNTRY VARCHAR(40),
    NO_OF_TERMINALS INT
);

-- Create the PASSENGER table
CREATE TABLE PASSENGER (
    PASSENGER_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    NAME VARCHAR(40),
    DATE_OF_BIRTH VARCHAR(40),
    PASSPORT_NO VARCHAR(40),
    PHONE_NO INT,
    EMAIL_ID VARCHAR(40)
);

-- Create the CREW table
CREATE TABLE CREW (
    CREW_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    NAME VARCHAR(40),
    POSITION VARCHAR(40),
    EXPERIENCE VARCHAR(40),
    AIRPORT_CODE VARCHAR(20),
    FOREIGN KEY (AIRPORT_CODE) REFERENCES AIRPORTS(AIRPORT_CODE)
);

-- Create the FLIGHT table
CREATE TABLE FLIGHT (
    FLIGHT_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    AIRCRAFT_ID VARCHAR(20),
    AIRCRAFT_COMPANY VARCHAR(40),
    ORIGIN VARCHAR(40),
    ARRIVAL_TIME VARCHAR(40),
    DEPARTURE_TIME VARCHAR(40),
    DESTINATION VARCHAR(40),
    FOREIGN KEY (AIRCRAFT_ID) REFERENCES AIRCRAFT(AIRCRAFT_ID)
);

-- Create the GATE table
CREATE TABLE GATE (
    GATE_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    AIRPORT_CODE VARCHAR(20),
    FOREIGN KEY (AIRPORT_CODE) REFERENCES AIRPORTS(AIRPORT_CODE)
);

-- Create the LUGGAGE table
CREATE TABLE LUGGAGE (
    LUGGAGE_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    PASSENGER_ID VARCHAR(20),
    WEIGHT FLOAT,
    LENGTH FLOAT,
    WIDTH FLOAT,
    HEIGHT FLOAT,
    STATUS VARCHAR(40),
    NO_OF_BAGS INT,
    FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID)
);

-- Create the BOOKING table
CREATE TABLE BOOKING (
    BOOKING_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    PASSENGER_ID VARCHAR(20),
    FLIGHT_ID VARCHAR(20),
    DATE_OF_BOOKING DATE,
    FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID),
    FOREIGN KEY (FLIGHT_ID) REFERENCES FLIGHT(FLIGHT_ID)
);

-- Create the SCHEDULE table
CREATE TABLE SCHEDULE (
    SCHEDULE_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    FLIGHT_ID VARCHAR(20),
    GATE_ID VARCHAR(20),
    ARRIVAL_TIME VARCHAR(40),
    DEPARTURE_TIME VARCHAR(40),
    FOREIGN KEY (FLIGHT_ID) REFERENCES FLIGHT(FLIGHT_ID),
    FOREIGN KEY (GATE_ID) REFERENCES GATE(GATE_ID)
);

-- Create the TICKET table
CREATE TABLE TICKET (
    TICKET_ID VARCHAR(20) PRIMARY KEY NOT NULL,
    FLIGHT_ID VARCHAR(20),
    PASSENGER_ID VARCHAR(20),
    SEAT_NO VARCHAR(40),
    PRICE INT,
    FOREIGN KEY (FLIGHT_ID) REFERENCES FLIGHT(FLIGHT_ID),
    FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID)
);

